HTML adalah singkatan dari Hypertext Markup Language. Pengertian HTML sering kali dikaitkan dengan bahasa pemrograman.

Akan tetapi, sejatinya, HTML bukan sebuah bahasa pemrograman (programming languange), melainkan sebuah markup languange.

Apakah kamu semakin bingung dengan penjelasan itu?

Apabila seorang penulis menulis dengan huruf A hingga Z serta angka, programmer bisa menggunakan tanda (mark) HTML untuk “menulis” sebuah halaman situs.

Tanda-tanda itu “disimpan” dalam sebuah “dokumen” HTML dan “diletakkan” di dalam server.
